# 1
#Given
name = "John Banks"
age = 34

# Result
# Hi, my name is John Banks and I am 34 years old

#printf
print(f"Hi, my name is {name} and I am {age} years old")
print(f'Hi, my name is {name} and I am {age} years old')

#print.format
print("Hi, my name is {} and I am {} years old".format(name, age))
print('Hi, my name is {} and I am {} years old'.format(name, age))


