# Operators

x = 20
y = 15

# Arithmetic
# Add
z = x + y
print(x + y)
print(z)

# Subtraction
print(x - y)

# Multiply
print(x * y)

# division
print(x / y)

# Modulus
print(x % y)

# Assignment Operators
print("Assignment Operators")

x += y
print(x)

x = 20

x -= y
print(x)

# Comparison
print("Comparison")

print("x:", x, "y:", y)

print(x == y)
print(x != y)
print(x <= y)
print(x > y)

# Logical operators
print("Logical Operators")

print(x < 50 and y < 50)
print(not(x < 10 and y < 30))
